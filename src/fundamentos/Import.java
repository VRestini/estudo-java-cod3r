package fundamentos;

import java.util.Date;

public class Import {
	public static void main(String[] args) {
		// ctrl shift o - arruma as importações
		String s = "bom dia";
		System.out.println(s);
		
		java.lang.String b = "bom dia";
		System.out.println(b);
		
		Date d = new Date();
		System.out.println(d);
		

	}
}
